/**
 * Інтерфейс для одної події активності.
 */
export interface ActivityEvent {
  /** Тип події: створення або редагування */
  type: 'created' | 'updated';

  /** Тип контенту: пост або проєкт */
  contentType: 'post' | 'project';

  /** Назва поста/проєкту */
  title: string;

  /** ID поста/проєкту */
  id: string;
}

/**
 * Інтерфейс для одного дня в календарі активності (Activity Heatmap).
 * Використовується для візуалізації активності створення/редагування контенту.
 */
export interface ActivityDay {
  /** Дата у форматі 'YYYY-MM-DD' */
  date: string;

  /** Рівень активності (0 = немає, 1-4 = інтенсивність) */
  level: number;

  /** Кількість подій у цей день (створення + редагування) */
  count: number;

  /** Список подій у цей день */
  events?: ActivityEvent[];
}
